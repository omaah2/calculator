buttons.forEach((item) => {
    item.onclick = () => {
        if (item.id == 'clear') {
            display.innerText = '';
        } else if (item.id == 'backspace') {
            let string = display.innerText.toString();
            display.innerText = string.substr(0, string.length = 1);
        } else if (display.innerText != '' && item.id == 'equal') {
            display.innerText = eval(display.innerText);
        } else if (display.innerText == '' && item.id == 'equal') {
            display.innerText = 'Empty!';
            setTimeout(() => (display.innerText = ''), 2000);
        } else {
            display.innerText += item.id;
        }
    }
})
const toggleThemeBtn = document.querySelector('.toggle-theme');
const calculator = document.querySelector('.calculator');
const toggleIcon = document.querySelector('.toggleIcon');
toggleThemeBtn.innerHTML = active_svg;
let isDark = true;
toggleThemeBtn.onclick = () => {
    calculator.classList.toggle('dark');
    toggleThemeBtn.classList.toggle('active');
    isDark = !isDark;

    // tenary operate used to change the svg icon, if isDark is true, active_svg would show otherwise low_svg would be picked
    toggleThemeBtn.innerHTML = isDark ? active_svg : low_svg;
}



